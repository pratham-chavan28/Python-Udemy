parrot = "Norwegian Blue"

print("-- Slicing")
print(parrot[0:6])
print(parrot[3:8])
print()

print("-- not giving either of the limit")
print(parrot[:6])
print(parrot[6:])
print()

print("-- not giving any of the limit")
print(parrot[:])
print()

print("-- negative indices")
print(parrot[-14:-8])
print(parrot[-4:-1])
print(parrot[-4:13])
print(parrot[-2:10])


